  def result_to_string(y)
    str = ''
    char = Array.new(y.size)
    for i in 0...y.size
      char[i] = [0, 0.0]
      for j in 0...y.first.size
        if y[i][j] > char[i][1]
          char[i][1] = y[i][j]
          char[i][0] = j
        end
      end
    end
    char = char.transpose[0]
    str = ''
    for i in 0...char.size
      if char[i] == 0
        str << ' '
      else
        str << 96 + char[i]
      end
    end
    str
  end
